package com.mateo.prglangrecognition.langrecognitionnet;

public class LangRecognitionException extends Exception {

    public LangRecognitionException() {
    }

    public LangRecognitionException(String message) {
        super(message);
    }

    public LangRecognitionException(String message, Throwable cause) {
        super(message, cause);
    }

    public LangRecognitionException(Throwable cause) {
        super(cause);
    }

}
